export function getScoreInterpretation(score: number, domain: string): string {
  if (score < 0) return "טרם מולא — אין מספיק נתונים";
  if (score >= 4.0) return "ציון גבוה — תחום זה מהווה חוזקה ומשאב משמעותי. מומלץ לשמר ולחזק.";
  if (score >= 3.0) return "ציון בינוני — תפקוד סביר. ניתן לחזק באמצעות ליווי ממוקד.";
  if (score >= 2.0) return "ציון מתחת לממוצע — נדרשת תשומת לב וליווי. מומלץ לבנות תכנית תמיכה.";
  return "ציון נמוך — דורש התערבות ותמיכה מיידית. מומלץ שיח אישי ובניית תכנית ייעודית.";
}
